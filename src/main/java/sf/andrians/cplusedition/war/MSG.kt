class MSG private constructor() : IResources {

    override fun getString(id: Int): String {
        return strings[id] ?: throw AssertionError(String.format("# string not found: 0x%08x", id))
    }

    override fun getStringArray(id: Int): Array<String> {
        val ret = getString(id)
        return ret.split("\n").toTypedArray()
    }

    override fun getFormatted(id: Int, vararg args: Any?): String {
        var ret = getString(id)
        var index = 0
        val len = args.size
        while (index < len) {
            val arg = args[index]
            ret = ret.replace("\${$index}", arg?.toString() ?: "null")
            ++index
        }
        return ret
    }

